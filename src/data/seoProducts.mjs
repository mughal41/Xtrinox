export function getSeoProductById(id) {
  return SEO_PRODUCTS.find((product) => product.id === id || product.slug === id) || null;
}

export function mergeSeoProductWithFirestoreTool(tool) {
  const seoProduct = getSeoProductById(tool?.id || tool?.slug);
  if (!seoProduct) return tool;

  return {
    ...seoProduct,
    ...tool,
    slug: tool?.slug || seoProduct.slug,
    name: tool?.name || seoProduct.name,
    description: tool?.description || seoProduct.description,
    category: tool?.category || seoProduct.category,
    logoUrl: tool?.logoUrl || seoProduct.logoUrl,
    imageUrl: seoProduct.imageUrl || tool?.imageUrl || '',
    bannerUrl: seoProduct.bannerUrl || tool?.bannerUrl || '',
    monthlyPrice: tool?.monthlyPrice || seoProduct.monthlyPrice,
    launchUrl: tool?.launchUrl || seoProduct.launchUrl,
    seoContent: tool?.seoContent || seoProduct.seoContent,
    featureHighlights: seoProduct.featureHighlights,
  };
}
